    private void apiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apiButtonActionPerformed
        try {

            URL url = new URL("http://127.0.0.1:5000/ia/"+apiText.getText());//your url i.e fetch data from .
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");
            conn.setRequestProperty("Accept", "application/json");
            if (conn.getResponseCode() != 200) {
                throw new RuntimeException("Failed : HTTP Error code : "
                        + conn.getResponseCode());
            }
            InputStreamReader in = new InputStreamReader(conn.getInputStream());
            BufferedReader br = new BufferedReader(in);
            String output;
            String resultados="";
            while ((output = br.readLine()) != null) {
                resultados+=output;
            }
            conn.disconnect();
            resultado.setText(resultados);
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null,"Exception in NetClientGet:- " + e);
        }
    }//GEN-LAST:event_apiButtonActionPerformed

